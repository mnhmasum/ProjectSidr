package com.atomix.datamodel;

import java.util.ArrayList;

public class ClassifiedInfo {
	private int id;
	private String title;
	private String description;
	private int catId;
	private int media;
	private int createdBy;
	private int updatedBy;
	private int isDel;
	private int status;
	private String date;
	private String updatedAt;
	private String createdAt;
	private int isDraft;
	
	private ClassifiedsOwnerInfo ownerInfo;
	private ArrayList<ClassifiedsPhotoInfo> photoInfo;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public int getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getCatId() {
		return catId;
	}

	public void setCatId(int catId) {
		this.catId = catId;
	}

	public int getMedia() {
		return media;
	}

	public void setMedia(int media) {
		this.media = media;
	}

	public int getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(int updatedBy) {
		this.updatedBy = updatedBy;
	}

	public int getIsDel() {
		return isDel;
	}

	public void setIsDel(int isDel) {
		this.isDel = isDel;
	}

	public String getUpdatedAt() {
		return updatedAt;
	}

	public void setUpdatedAt(String updatedAt) {
		this.updatedAt = updatedAt;
	}

	public String getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}

	public ClassifiedsOwnerInfo getOwnerInfo() {
		return ownerInfo;
	}

	public void setOwnerInfo(ClassifiedsOwnerInfo ownerInfo) {
		this.ownerInfo = ownerInfo;
	}

	public ArrayList<ClassifiedsPhotoInfo> getPhotoInfo() {
		return photoInfo;
	}

	public void setPhotoInfo(ArrayList<ClassifiedsPhotoInfo> photoInfo) {
		this.photoInfo = photoInfo;
	}

	public int getIsDraft() {
		return isDraft;
	}

	public void setIsDraft(int isDraft) {
		this.isDraft = isDraft;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
	
}

	
