package com.atomix.datamodel;

import java.util.ArrayList;

public class ForumsInfo {
	private int id;
	private String text;
	private int createdBy;
	private int updatedBy;
	private String updatedAt;
	private String createdAt;
	private int isDel;
	private int status;
	private AuthorInfo authorInfo;
	private ArrayList<ForumPhotoInfo> forumPhotos;
	private ArrayList<ForumComments> forumComments;
	
	private String date;
	
	private int totalComments;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getTotalComments() {
		return totalComments;
	}

	public void setTotalComments(int totalComments) {
		this.totalComments = totalComments;
	}

	public String getUpdatedAt() {
		return updatedAt;
	}

	public void setUpdatedAt(String updatedAt) {
		this.updatedAt = updatedAt;
	}

	public String getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}

	public int getIsDel() {
		return isDel;
	}

	public void setIsDel(int isDel) {
		this.isDel = isDel;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public AuthorInfo getAuthorInfo() {
		return authorInfo;
	}

	public void setAuthorInfo(AuthorInfo authorInfo) {
		this.authorInfo = authorInfo;
	}

	public ArrayList<ForumPhotoInfo> getForumPhotos() {
		return forumPhotos;
	}

	public void setForumPhotos(ArrayList<ForumPhotoInfo> forumPhotos) {
		this.forumPhotos = forumPhotos;
	}

	public ArrayList<ForumComments> getForumComments() {
		return forumComments;
	}

	public void setForumComments(ArrayList<ForumComments> forumComments) {
		this.forumComments = forumComments;
	}

	public int getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}

	public int getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(int updatedBy) {
		this.updatedBy = updatedBy;
	}

}
